/**
 * Find a YouTube ID from cache using fuzzy matching on artist + title
 */
export function findInCache(artist, title) {
  const normalize = (s) => s.toLowerCase()
    .replace(/[^a-z0-9\s\-]/g, '')
    .replace(/\s+/g, ' ')
    .trim();

  const artistN = normalize(artist);
  const titleN = normalize(title);

  // Try "artist title"
  const full = `${artistN} ${titleN}`;
  if (ytCache[full]) return ytCache[full];

  // Try "title" alone
  if (ytCache[titleN]) return ytCache[titleN];

  // Try "artist title" with just first word of artist
  const firstArtistWord = artistN.split(' ')[0];
  const partial = `${firstArtistWord} ${titleN}`;
  if (ytCache[partial]) return ytCache[partial];

  // Fuzzy: find any cache key that contains the title
  const titleWords = titleN.split(' ').filter(w => w.length > 2);
  for (const [key, id] of Object.entries(ytCache)) {
    if (titleWords.every(w => key.includes(w))) return id;
  }

  return null;
}
